# Get the first test score.
first_score = float(input("Enter your first test score: "))

# Get the second test score. 
second_score = float(input("Enter your second test score: "))

# Get the third test score.
third_score = float(input("Enter your third test score: "))

# Calculate the average by adding the three test scores and dividing the sum by 3. 
average = (first_score + second_score + third_score) / 3.0

# Display the average. 
print('Your average is', average)
